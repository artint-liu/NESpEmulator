def get_list(pattern, string):
    ret = re.findall(pattern, string)[0]
    ret = ret.replace('"', '')
    ret = ret.split(",")
    ret.pop()

    return ret


def map_addressing(addressing_modes):
    m = {
        "1": "Abs",
        "2": "Abx",
        "3": "Aby",
        "4": "Acc",
        "5": "Imm",
        "6": "Imp",
        "7": "Izx",
        "8": "Ind",
        "9": "Izy",
        "10": "Rel",
        "11": "Zp0",
        "12": "Zpx",
        "13": "Zpy",
    }
    ret = []
    for addressing_mode in addressing_modes:
        ret.append(m[addressing_mode.strip()])
    return ret


def get_result(names, addressing_modes, instructions, cycles, page_cycles):
    table = ["ADC", "AND", "ASL", "BCC", "BCS", "BEQ", "BIT", "BMI", "BNE", "BPL", "BRK", "BVC", "BVS", "CLC", "CLD",
             "CLI", "CLV", "CMP", "CPX", "CPY", "DEC", "DEX", "DEY", "EOR", "INC", "INX", "INY", "JMP",
             "JSR", "LDA", "LDX", "LDY", "LSR", "NOP", "ORA", "PHA", "PHP", "PLA", "PLP", "ROL", "ROR", "RTI", "RTS",
             "SBC", "SEC", "SED", "SEI", "STA", "STX", "STY", "TAX", "TAY", "TSX", "TXA", "TXS", "TYA"]

    res = ""
    for i in range(256):
        if names[i].strip() not in table:
            names[i] = "???"
            instructions[i] = "&CPU::NIL"

        # {"BRK", Imp, &CPU::BRK, 0, 0},
        res += f'Operation{{.name = "{names[i].strip()}", .addressing = Addressing::{addressing_modes[i].strip()}, .instruction = {instructions[i].strip()}, .cycle = {cycles[i].strip()}, .pageCycle = {page_cycles[i].strip()}}},\n'
    return res


